var add = function(num1, num2){
    return num1+num2
}

var subtract = function(num1, num2){
    return num1-num2
}

var multiply = function(num1, num2){
    return num1*num2
}

var divide = function(num1, num2){
    return num1/num2
}

console.log(add(20,24))
console.log(subtract(100,91))
console.log(multiply(7,70))
console.log(divide(33,11))